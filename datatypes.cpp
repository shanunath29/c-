#include<iostream>
using namespace std;

int main(){
    //integer value
    int integerValue = 20;
    cout<<"Stored integer value is: "<<integerValue<<endl;

    // Floating point datatype
    float floatValue = 3.14;
    cout<<"stored float value is: "<<floatValue<<endl;

    // Character datatypes
    char charValue = 'S';
    cout<<"Stored character value is: "<<charValue<<endl;

    // Boolean datatype
    bool boolValue = false;
    cout<<"Stored boolean value is: "<<boolValue<<endl;

    return 0;
}